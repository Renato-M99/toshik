package ex1;

public class Dados_Condominio {

	private String name;
	private String apartamento;
	private String taxaZelador;
	private String taxaLavanderia;
	private String aluguel;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getApartamento() {
		return apartamento;
	}
	public void setApartamento(String apartamento) {
		this.apartamento = apartamento;
	}
	public String getTaxaZelador() {
		return taxaZelador;
	}
	public void setTaxaZelador(String taxaZelador) {
		this.taxaZelador = taxaZelador;
	}
	public String getTaxaLavanderia() {
		return taxaLavanderia;
	}
	public void setTaxaLavanderia(String taxaLavanderia) {
		this.taxaLavanderia = taxaLavanderia;
	}
	public String getAluguel() {
		return aluguel;
	}
	public void setAluguel(String aluguel) {
		this.aluguel = aluguel;
	}

}
